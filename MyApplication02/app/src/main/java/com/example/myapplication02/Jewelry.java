public class Jewelry {

    private String name;
    private int imageId;

   public Jewelry(String name,int imageId){
       this.name=name;
       this.imageId=imageId;

   }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}

